package com.apple.carnival.qa.coreframework.data.pageobjects;

public class WD40Host {
	
	private String Host = null;
	private String State = null;
	private String ISAppNames = null;
	private String HostPhysicalRAM = null;
	private String SubscribedRAM = null;
	private String SubscribedRAMPct = null;
	private String Select = null;
	public String getHost() {
		return Host;
	}
	public void setHost(String host) {
		Host = host;
	}
	public String getState() {
		return State;
	}
	public void setState(String state) {
		State = state;
	}
	public String getISAppNames() {
		return ISAppNames;
	}
	public void setISAppNames(String iSAppNames) {
		ISAppNames = iSAppNames;
	}
	public String getHostPhysicalRAM() {
		return HostPhysicalRAM;
	}
	public void setHostPhysicalRAM(String hostPhysicalRAM) {
		HostPhysicalRAM = hostPhysicalRAM;
	}
	public String getSubscribedRAM() {
		return SubscribedRAM;
	}
	public void setSubscribedRAM(String subscribedRAM) {
		SubscribedRAM = subscribedRAM;
	}
	public String getSubscribedRAMPct() {
		return SubscribedRAMPct;
	}
	public void setSubscribedRAMPct(String subscribedRAMPct) {
		SubscribedRAMPct = subscribedRAMPct;
	}
	public String getSelect() {
		return Select;
	}
	public void setSelect(String select) {
		Select = select;
	}
}
